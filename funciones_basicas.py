print("--- Funciones Básicas de Python ---") # Imprime un título en la consola para nuestra prueba.
numero = 10 # Crea una variable llamada 'numero' y le asigna el valor entero 10.
print("Tipo de dato de numero:", type(numero)) # Utiliza type() para obtener el tipo de dato de 'numero' y lo imprime.
texto = "Hola Mundo" # Crea una variable de texto llamada 'texto'.
print("Longitud del texto:", len(texto)) # Utiliza len() para contar los caracteres de 'texto' y lo imprime.
numero_texto = "50" # Crea una variable de texto que contiene un número.
numero_entero = int(numero_texto) # Utiliza int() para convertir el texto a un número entero.
print("Numero convertido:", numero_entero) # Imprime el nuevo número entero.
lista = [1, 2, 3, 4, 5] # Crea una lista con varios números enteros.
print("Suma de la lista:", sum(lista)) # Utiliza sum() para sumar todos los números de la lista y lo imprime.
print("Maximo de la lista:", max(lista)) # Utiliza max() para encontrar el valor más alto de la lista y lo imprime.
print("Minimo de la lista:", min(lista)) # Utiliza min() para encontrar el valor más bajo de la lista y lo imprime.
